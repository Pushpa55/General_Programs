package com.sritech.google.test;

public class SizeAndRangeValues {
	public static void main(String[] args) {

		System.out.println ("Character size :"+Character.SIZE+"\t Min value : "+Character.MIN_VALUE+"\t Max value : "+Character.MAX_VALUE);
	
		System.out.println ("Byte size :"+Byte.SIZE+"\t Min value : "+Byte.MIN_VALUE+"\t Max value : "+Byte.MAX_VALUE);
		
		System.out.println("Short size:"+Short.SIZE+"\t Min value:"+Short.MIN_VALUE+"\t Max value:"+Short.MAX_VALUE);
		
		System.out.println("Integer size:"+Integer.SIZE+"\t Min value:"+Integer.MIN_VALUE+"\t Max value:"+Integer.MAX_VALUE);
		
		System.out.println("Long size:"+Long.SIZE+"\t Min value:"+Long.MIN_VALUE+"\t Max value:"+Long.MAX_VALUE);
		
		System.out.println("Float size:"+Float.SIZE+"\t Min value:"+Float.MIN_VALUE+"\t Max value:"+Float.MAX_VALUE);
 
		System.out.println("Double size:"+Double.SIZE+"\t Min value:"+Double.MIN_VALUE+"\t Max value:"+Double.MAX_VALUE);
	}

}
