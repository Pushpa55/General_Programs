package com.sritech.google.test;

public class IncrementAndDec {
	public static void main(String[] args) 
	{		
			int a=10;
			
			int b;
			
			int c;
			
			int d;
			
			b=++a;   //a=11,b=11
			
			c=b++;   //b=12,c=11
			
			d=++c;   //c=12,d=12
			
			System.out.println (a+"\t"+b+"\t"+c+"\t"+d);
		}


}
